
const Engine = Matter.Engine;
const World = Matter.World;
const Bodies = Matter.Bodies;
const Body = Matter.Body;
var d1,d2,d3,garbage,ground;



function setup() {
	engine = Engine.create();
	world = engine.world;

	createCanvas(1600, 700);

    ground = createSprite(800,650,1600,20);
	
	
	

	d1 = createSprite(1200,600,20,100);
	d1.shapeColor = "red";
	d2 = createSprite(1300,645,200,20);
	d2.shapeColor = "red";
	d3 = createSprite(1400,600,20,100);
	d3.shapeColor = "red";
	ground.shapeColor = "brown";
	
	

	paper = new Garbage(200,620);

	



	Engine.run(engine);
  
}


function draw() {
  rectMode(CENTER);
  background(0);

  paper.display();

  
  
  drawSprites();
 
}

function keyPressed()
{
	if (keyCode === UP_ARROW)
	{
		Matter.Body.applyForce(paper.body,paper.body.position,{x:85,y:-85});
		
	}
}








































































































































































































































































































































































































